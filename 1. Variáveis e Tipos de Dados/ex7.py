# Exercício 7 – Diferença de Idades
# 1. Crie duas variáveis com a idade de duas pessoas
# 2. Calcule a diferença de idade
# 3. Mostre a frase: "A diferença de idade é de 4 anos"

# ESCREVA SEU CÓDIGO AQUI

idade = 21
idade2 = 25

diferenca = (idade - idade2)

# converte para um numero positivo
diferenca_positiva = abs(diferenca)

print(f'A diferença de idade é de {diferenca_positiva} anos')