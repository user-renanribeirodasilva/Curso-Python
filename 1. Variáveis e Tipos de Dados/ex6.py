# Exercício 6 – Média de Notas
# 1. Crie 3 variáveis com as notas de um aluno (float)
# 2. Calcule a média das notas
# 3. Mostre a frase: "A média do aluno é 7.50", formatando com 2 casas decimais

# ESCREVA SEU CÓDIGO AQUI

n1 = 8.7
n2 = 6.5
n3 = 6.9

media = (n1 + n2 + n3) / 3

print(f'A média do aluno é {media:.2f}')